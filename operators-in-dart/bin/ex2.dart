void main(List<String> args) {
  int age = 50;
  // print(age / 20);
  // print(age ~/ 20);
  // print(age % 20);

  // bitwise infix operators

  // bitwise AND
  print(age & 20);

  // bitwise OR
  print(age | 20);

  // bitwise XOR
  print(age ^ 20);

  //bitwise left shift
  print(age << 20);

  //bitwise right shift
  print(age >> 20);
}
